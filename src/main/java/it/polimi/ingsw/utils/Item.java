/**
 * Enumeration of all possible types of item tiles. They are part of the {@code LivingRoom} and of the {@code Bookshelf}.
 * This enumeration is also used by other the view package and the controller package.
 */
public enum Item {
    CAT,
    BOOK,
    GAME,
    FRAME,
    CUP,
    PLANT,
    LOCKED
}
